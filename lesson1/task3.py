"""
3)	Узнайте у пользователя число n. Найдите сумму чисел n + nn + nnn.
Например, пользователь ввёл число 3. Считаем 3 + 33 + 333 = 369.
"""

n = input("Введите целое число: ")
n1 = int(n)
n2 = int(f"{n}{n}")
n3 = int(f"{n}{n}{n}")
sum_n = n1 + n2 + n3
print(f"сумма чисел n + nn + nnn = {n1}+{n2}+{n3} = {sum_n }")
